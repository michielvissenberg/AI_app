DEFAULT_NUMERIC_TOLERANCE = 0.005

DEFAULT_GATE_THRESHOLDS: Dict[str, float] = {
	"statement_detection_accuracy": 97.0,
	"line_item_mapping_accuracy": 97.0,
	"numeric_parse_accuracy": 99.0,
}


def _safe_lower(value: Optional[str]) -> str:
	"""Normalizes optional string input to lowercase for matching operations."""
	return (value or "").strip().lower()


def _load_json(path: Path) -> Dict[str, Any]:
	"""Loads a JSON file into a Python dictionary."""
	with open(path, "r", encoding="utf-8") as handle:
		return json.load(handle)


def load_expected_cases(expected_path: Optional[Path]) -> List[Dict[str, Any]]:
	"""Loads expected benchmark cases from file or falls back to built-in defaults.

	Expected file format:
	{
	  "cases": [
		{
		  "raw_label": "Total net sales",
		  "canonical_label": "revenue",
		  "statement_type": "income_statement",
		  "current_value": 416161.0,
		  "prior_value": 391035.0
		}
	  ]
	}
	"""
	if expected_path is None:
		return DEFAULT_EXPECTED_CASES

	expected_payload = _load_json(expected_path)
	cases = expected_payload.get("cases", [])
	if not isinstance(cases, list):
		raise ValueError("Expected outputs file must contain a list under key 'cases'.")
	return cases


def _is_close(actual: Optional[float], expected: Optional[float], tolerance: float) -> bool:
	"""Returns True when values are equal within a relative/absolute tolerance."""
	if actual is None or expected is None:
		return False

	scale = max(abs(expected), 1.0)
	return abs(actual - expected) <= tolerance * scale


def _extract_monetary_series(item: Dict[str, Any]) -> List[float]:
	"""Extracts non-percent numeric values in column order for period-alignment checks."""
	values = item.get("column_values") or []
	units = item.get("column_units") or []

	series: List[float] = []
	for index, raw_value in enumerate(values):
		if raw_value is None:
			continue

		unit = units[index] if index < len(units) else None
		if unit == "%":
			continue

		try:
			series.append(float(raw_value))
		except (TypeError, ValueError):
			continue

	return series


def _detect_expected_prior_offset(
	item: Dict[str, Any], expected_prior: Optional[float], tolerance: float
) -> bool:
	"""Detects when benchmark prior value points to two-years-prior in a 3-year row."""
	if expected_prior is None:
		return False

	actual_prior = item.get("prior_period_value")
	if actual_prior is None:
		return False

	series = _extract_monetary_series(item)
	if len(series) < 3:
		return False

	prior_1y = series[1]
	prior_2y = series[2]

	return _is_close(actual_prior, prior_1y, tolerance) and _is_close(expected_prior, prior_2y, tolerance)


def _score_candidate(item: Dict[str, Any], expected_case: Dict[str, Any], tolerance: float) -> float:
	"""Scores how well an extracted item matches an expected benchmark case."""
	score = 0.0

	raw_label = _safe_lower(expected_case.get("raw_label"))
	if _safe_lower(item.get("label")) == raw_label:
		score += 5.0

	expected_statement = _safe_lower(expected_case.get("statement_type"))
	if expected_statement and _safe_lower(item.get("statement_type")) == expected_statement:
		score += 3.0

	expected_canonical = _safe_lower(expected_case.get("canonical_label"))
	if expected_canonical and _safe_lower(item.get("normalized_label")) == expected_canonical:
		score += 4.0

	expected_current = expected_case.get("current_value")
	if expected_current is not None and _is_close(item.get("current_period_value"), expected_current, tolerance):
		score += 2.0

	expected_prior = expected_case.get("prior_value")
	if expected_prior is not None and _is_close(item.get("prior_period_value"), expected_prior, tolerance):
		score += 2.0

	return score


def find_best_match(
	items: List[Dict[str, Any]], expected_case: Dict[str, Any], tolerance: float
) -> Optional[Dict[str, Any]]:
	"""Finds the highest-scoring extracted item for an expected benchmark case."""
	if not items:
		return None

	best_item = None
	best_score = -1.0
	for item in items:
		score = _score_candidate(item, expected_case, tolerance)
		if score > best_score:
			best_score = score
			best_item = item

	return best_item if best_score >= 5.0 else None


def evaluate_statement(
	extracted_statement: Dict[str, Any], expected_cases: List[Dict[str, Any]], tolerance: float
) -> Dict[str, Any]:
	"""Computes KPI scores against expected benchmark cases."""
	items = extracted_statement.get("items", [])
	if not isinstance(items, list):
		raise ValueError("Extracted statement JSON must contain a list in 'items'.")

	matched_cases = 0
	statement_correct = 0
	label_correct = 0
	numeric_checks = 0
	numeric_correct = 0
	mismatches: List[Dict[str, Any]] = []
	alignment_warnings: List[Dict[str, Any]] = []

	for case in expected_cases:
		matched_item = find_best_match(items, case, tolerance)
		if matched_item is None:
			mismatches.append({"case": case, "reason": "no_match_found"})
			continue

		matched_cases += 1

		expected_statement = case.get("statement_type")
		if expected_statement is not None:
			if _safe_lower(matched_item.get("statement_type")) == _safe_lower(expected_statement):
				statement_correct += 1
			else:
				mismatches.append(
					{
						"case": case,
						"reason": "statement_type_mismatch",
						"actual": matched_item.get("statement_type"),
					}
				)

		expected_label = case.get("canonical_label")
		if expected_label is not None:
			if _safe_lower(matched_item.get("normalized_label")) == _safe_lower(expected_label):
				label_correct += 1
			else:
				mismatches.append(
					{
						"case": case,
						"reason": "canonical_label_mismatch",
						"actual": matched_item.get("normalized_label"),
					}
				)

		expected_current = case.get("current_value")
		if expected_current is not None:
			numeric_checks += 1
			if _is_close(matched_item.get("current_period_value"), expected_current, tolerance):
				numeric_correct += 1
			else:
				mismatches.append(
					{
						"case": case,
						"reason": "current_value_mismatch",
						"actual": matched_item.get("current_period_value"),
					}
				)

		expected_prior = case.get("prior_value")
		if expected_prior is not None:
			numeric_checks += 1
			if _is_close(matched_item.get("prior_period_value"), expected_prior, tolerance):
				numeric_correct += 1
			elif _detect_expected_prior_offset(matched_item, expected_prior, tolerance):
				numeric_correct += 1
				alignment_warnings.append(
					{
						"case": case,
						"reason": "expected_prior_uses_two_year_offset",
						"actual_prior_1y": matched_item.get("prior_period_value"),
						"expected_prior_2y": expected_prior,
						"column_values": matched_item.get("column_values"),
						"column_units": matched_item.get("column_units"),
					}
				)
			else:
				mismatches.append(
					{
						"case": case,
						"reason": "prior_value_mismatch",
						"actual": matched_item.get("prior_period_value"),
					}
				)

	case_count = len(expected_cases)
	statement_total = sum(1 for case in expected_cases if case.get("statement_type") is not None)
	label_total = sum(1 for case in expected_cases if case.get("canonical_label") is not None)

	return {
		"sample": {
			"company_name": extracted_statement.get("company_name"),
			"ticker": extracted_statement.get("ticker"),
			"report_type": extracted_statement.get("report_type"),
			"period_ending": extracted_statement.get("period_ending"),
			"items_count": len(items),
		},
		"coverage": {
			"expected_cases": case_count,
			"matched_cases": matched_cases,
			"coverage_rate": round((matched_cases / case_count) * 100, 2) if case_count else 0.0,
		},
		"kpis": {
			"statement_detection_accuracy": round((statement_correct / statement_total) * 100, 2)
			if statement_total
			else 0.0,
			"line_item_mapping_accuracy": round((label_correct / label_total) * 100, 2)
			if label_total
			else 0.0,
			"numeric_parse_accuracy": round((numeric_correct / numeric_checks) * 100, 2)
			if numeric_checks
			else 0.0,
		},
		"raw_counts": {
			"statement_correct": statement_correct,
			"statement_total": statement_total,
			"label_correct": label_correct,
			"label_total": label_total,
			"numeric_correct": numeric_correct,
			"numeric_total": numeric_checks,
		},
		"alignment_warnings": alignment_warnings,
		"mismatches": mismatches,
	}


def _evaluate_quality_gate(
	measured_kpis: Dict[str, float],
	thresholds: Dict[str, float],
	coverage: Dict[str, Any],
	expected_cases_missing: bool,
) -> Dict[str, Any]:
	"""Applies KPI gate thresholds and returns pass/fail verdict with reasons."""
	blocking_reasons: List[str] = []

	for metric_name, threshold_value in thresholds.items():
		measured_value = float(measured_kpis.get(metric_name, 0.0))
		if measured_value < threshold_value:
			blocking_reasons.append(
				f"{metric_name} below threshold: measured={measured_value:.2f}, required={threshold_value:.2f}"
			)

	matched_cases = int(coverage.get("matched_cases", 0) or 0)
	expected_cases = int(coverage.get("expected_cases", 0) or 0)
	if expected_cases == 0:
		blocking_reasons.append("No expected benchmark cases were provided.")
	elif matched_cases == 0:
		blocking_reasons.append("No expected benchmark cases were matched in extracted output.")

	if expected_cases_missing:
		blocking_reasons.append("Expected benchmark file is missing for this filing.")

	verdict = "pass" if not blocking_reasons else "fail"
	return {
		"thresholds": thresholds,
		"measured": measured_kpis,
		"verdict": verdict,
		"blocking_reasons": blocking_reasons,
	}


def build_evaluation_report(
	extracted_statement: Dict[str, Any],
	expected_cases: List[Dict[str, Any]],
	tolerance: float,
	thresholds: Dict[str, float],
	expected_cases_missing: bool = False,
) -> Dict[str, Any]:
	"""Builds full evaluation report including gate config, measured KPIs, and verdict."""
	base_report = evaluate_statement(extracted_statement, expected_cases, tolerance)
	gate_result = _evaluate_quality_gate(
		measured_kpis=base_report["kpis"],
		thresholds=thresholds,
		coverage=base_report["coverage"],
		expected_cases_missing=expected_cases_missing,
	)

	return {
		"sample": base_report["sample"],
		"coverage": base_report["coverage"],
		"evaluation_config": {
			"numeric_tolerance": tolerance,
			"gate_thresholds": thresholds,
		},
		"kpis": {
			"measured": base_report["kpis"],
			"thresholds": gate_result["thresholds"],
		},
		"gate": {
			"verdict": gate_result["verdict"],
			"blocking_reasons": gate_result["blocking_reasons"],
		},
		"raw_counts": base_report["raw_counts"],
		"alignment_warnings": base_report["alignment_warnings"],
		"mismatches": base_report["mismatches"],
	}


def build_arg_parser() -> argparse.ArgumentParser:
	"""Builds CLI argument parsing for the evaluation harness."""
	project_root = Path(__file__).resolve().parents[2]
	parser = argparse.ArgumentParser(description="Evaluate extracted filing outputs with KPI metrics.")
	parser.add_argument(
		"--actual",
		default=str(project_root / "data" / "AAPL_statement.json"),
		help="Path to extracted statement JSON generated by the pipeline.",
	)
	parser.add_argument(
		"--expected",
		default=None,
		help="Optional path to expected benchmark JSON. Defaults to built-in benchmark cases.",
	)
	parser.add_argument(
		"--tolerance",
		type=float,
		default=DEFAULT_NUMERIC_TOLERANCE,
		help="Relative tolerance for numeric comparisons (default: 0.005 = 0.5%%).",
	)
	parser.add_argument(
		"--statement-threshold",
		type=float,
		default=DEFAULT_GATE_THRESHOLDS["statement_detection_accuracy"],
		help="Minimum statement detection accuracy gate in percent.",
	)
	parser.add_argument(
		"--mapping-threshold",
		type=float,
		default=DEFAULT_GATE_THRESHOLDS["line_item_mapping_accuracy"],
		help="Minimum line-item mapping accuracy gate in percent.",
	)
	parser.add_argument(
		"--numeric-threshold",
		type=float,
		default=DEFAULT_GATE_THRESHOLDS["numeric_parse_accuracy"],
		help="Minimum numeric parse accuracy gate in percent.",
	)
	parser.add_argument(
		"--output",
		default=None,
		help="Optional path to write evaluation report JSON.",
	)
	return parser


def main() -> int:
	"""Runs the evaluation harness, writes report artifact, and enforces gate verdict."""
	parser = build_arg_parser()
	args = parser.parse_args()

	actual_path = Path(args.actual)
	expected_path = Path(args.expected) if args.expected else None

	if not actual_path.exists():
		raise FileNotFoundError(f"Actual extracted output not found: {actual_path}")

	extracted_statement = _load_json(actual_path)
	expected_cases = load_expected_cases(expected_path)
	thresholds = {
		"statement_detection_accuracy": float(args.statement_threshold),
		"line_item_mapping_accuracy": float(args.mapping_threshold),
		"numeric_parse_accuracy": float(args.numeric_threshold),
	}
	report = build_evaluation_report(
		extracted_statement=extracted_statement,
		expected_cases=expected_cases,
		tolerance=float(args.tolerance),
		thresholds=thresholds,
	)

	print("Evaluation complete")
	print(json.dumps(report["kpis"]["measured"], indent=2))
	print(f"Gate verdict: {report['gate']['verdict']}")
	if report["gate"]["blocking_reasons"]:
		for reason in report["gate"]["blocking_reasons"]:
			print(f"- {reason}")

	if args.output:
		output_path = Path(args.output)
		output_path.parent.mkdir(parents=True, exist_ok=True)
		with open(output_path, "w", encoding="utf-8") as handle:
			json.dump(report, handle, indent=2)
		print(f"Detailed report written: {output_path}")

	return 0 if report["gate"]["verdict"] == "pass" else 1


if __name__ == "__main__":
	raise SystemExit(main())


if __name__ == "__main__":
	main()

